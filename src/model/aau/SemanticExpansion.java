package aau;

public class SemanticExpansion {
	
	public SemanticExpansion(Tag tag, Resource kiWiResource, String context, String semanticExpansion) {
		super();
		this.tag = tag;
		this.context = context;
		this.kiWiResource = kiWiResource;
		this.semanticExpansion = semanticExpansion;
	}
	
	private String semanticExpansion;
	
	private String context;
	
	private Tag tag;

	private Resource kiWiResource;
	
	private String semanticProperty;
	
		
	public String getSemanticProperty() {
		return semanticProperty;
	}

	public void setSemanticProperty(String semanticProperty) {
		this.semanticProperty = semanticProperty;
	}

	public String getContext() {
		return context;
	}

	public void setContext(String context) {
		this.context = context;
	}

	public Resource getKiWiResource() {
		return kiWiResource;
	}

	public void setKiWiResource(Resource kiWiResource) {
		this.kiWiResource = kiWiResource;
	}

	public Tag getTag() {
		return tag;
	}

	public void setTag(Tag tag) {
		this.tag = tag;
	}

	public String getSemanticExpansion() {
		return semanticExpansion;
	}

	public void setSemanticExpansion(String semanticExpansion) {
		this.semanticExpansion = semanticExpansion;
	}


	

}
